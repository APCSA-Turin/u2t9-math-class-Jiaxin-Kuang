public class SinCosTan {
    public SinCosTan(){}
    public double Sin(double firstNum, double secondNum){
        return firstNum * Math.sin(secondNum);
    }
    public double Cos(double firstNum, double secondNum){
        return firstNum * Math.cos(secondNum);
    }
    public double Tan(double firstNum, double secondNum){
        return firstNum * Math.tan(secondNum);
    }
    public double Sin(double onlyNum){
        return Math.sin(onlyNum);
    }
    public double Cos(double onlyNum){
        return Math.cos(onlyNum);
    }
    public double Tan(double onlyNum){
        return Math.tan(onlyNum);
    }
    public double Asin(double firstNum, double secondNum){
        return firstNum * Math.asin(secondNum);
    }
    public double Acos(double firstNum, double secondNum){
        return firstNum * Math.acos(secondNum);
    }
    public double Atan(double firstNum, double secondNum){
        return firstNum * Math.atan(secondNum);
    }
    public double Asin(double onlyNum){
        return Math.asin(onlyNum);
    }
    public double Acos(double onlyNum){
        return Math.acos(onlyNum);
    }
    public double Atan(double onlyNum){
        return Math.atan(onlyNum);
    }
}
